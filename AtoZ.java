package demo;

public class AtoZ {
	public static void main(String args[]) {
		char Alpha,Beta;
		System.out.println("CAPITAL ALPHABETS");
		for(char i=65;i<=90;i++) {
			Alpha=i;
			System.out.println(i);
		}
		System.out.println("SMALL ALPHABETS");
		for(char j=97;j<=122;j++) {
			Beta=j;
			System.out.println(Beta);
		}
	}
}
